def toPdb(system, outfile="test.pdb", element=0):
    """ output to pdb file format
    """
    o = open(outfile, 'w')
    #o.write("TITLE %s\n"%system.name)
    o.write("REMARK Generated by simpy\n")
    #print system.pbc
    if system.pbc:
        o.write("CRYST1")
        o.write("".join(["%9.3f"%i for i in system.pbc[:3]]))
        o.write("".join(["%7.2f"%i for i in system.pbc[3:]]))
        o.write(" P 1           1\n")
    o.write("MODEL        1\n")
    counter = 1
    for i in system.atoms:
        o.write("HETATM")
        n_id = counter
        if counter > 99999: 
            n_id = counter/1000
        o.write("%5d"%n_id)
        if len(i.name) > 4:
            i.name = i.name[:4]
        if element:
            i.name = i.element
        o.write("%4s"%i.name)
        o.write("%5s"%"LIG")
        if counter > 99999: 
            n_id = counter/1000
        o.write("%6d"%n_id)
        o.write("%12.2f"%i.x[0])
        o.write("%8.2f"%i.x[1])
        o.write("%8.2f"%i.x[2])
        o.write("%6.2f"%1.0)
        o.write("%6.2f"%0.0)
        o.write("%12s"%i.name)
        o.write("\n")
        counter += 1
    o.write("TER\n")
    o.write("ENDMDL\n")
    o.close()

